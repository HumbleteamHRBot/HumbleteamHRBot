def search_knowledge(query: str, top_k: int = 3) -> list[dict]:
    """Поиск релевантных статей по запросу."""
    q = query.lower()
    words = [w for w in q.split() if len(w) > 3]
    results = []

    for cat in KNOWLEDGE_BASE["categories"]:
        for article in cat["articles"]:
            score = 0
            for kw in article["keywords"]:
                if kw in q:
                    score += 3
            for w in words:
                for kw in article["keywords"]:
                    if w in kw or kw in w:
                        score += 1
                if w in article["content"].lower():
                    score += 0.5
                if w in article["title"].lower():
                    score += 2
            if score > 0:
                results.append({
                    **article,
                    "score": score,
                    "category": cat["title"],
                    "icon": cat["icon"],
                })

    results.sort(key=lambda x: x["score"], reverse=True)
    return results[:top_k]


def search_faq(query: str, top_k: int = 3) -> list[dict]:
    """Поиск релевантных FAQ по запросу."""
    q = query.lower()
    words = [w for w in q.split() if len(w) > 2]
    results = []

    for item in FAQ:
        text = (item["q"] + " " + item["a"]).lower()
        if any(w in text for w in words) or q in text:
            results.append(item)

    return results[:top_k]
